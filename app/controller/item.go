func (c *itemCtl) GetItemList(ctx *gin.Context) {
	// 查询站点列表
	list := make([]model.Item, 0)
	utils.XormDb.Where("status=1 and mark=1").OrderBy("sort asc").Find(&list)
	// 返回结果
	ctx.JSON(http.StatusOK, common.JsonResult{
		Code: 0,
		Msg:  "查询成功",
		Data: list,
	})
}
